package com.app.shopping.ecommerce.util;

public class ImageDimensions {
    private int width;
    private int height;

    public ImageDimensions(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}